/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {TreeNode} root
 * @return {void} Do not return anything, modify root in-place instead.
 */
var recoverTree = function(root) {
  let firstErrorNode = null
  let secondErrorNode = null
  let previousNode = null

  const travel = (node, cb) => {
    if (node.left) {
      travel(node.left, cb)
    }
    cb(node)
    if (node.right) {
      travel(node.right, cb)
    }
  }

  travel(root, node => {
    if (previousNode) {
      if (previousNode.val > node.val) {
        if (!firstErrorNode) {
          firstErrorNode = previousNode
        }
        secondErrorNode = node
      }
    }
    previousNode = node
  })

  const temp = firstErrorNode.val
  firstErrorNode.val = secondErrorNode.val
  secondErrorNode.val = temp
};
