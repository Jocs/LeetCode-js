/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {TreeNode} root
 * @param {number} targetSum
 * @return {number[][]}
 */
var pathSum = function (root, targetSum) {
  if (!root) return []

  const result = []
  let sum = 0
  const path = []

  const travel = (node, preHandle, postHandle) => {
    preHandle(node)
    if (node.left)
      travel(node.left, preHandle, postHandle)
    if (node.right)
      travel(node.right, preHandle, postHandle)
    postHandle(node)
  }

  travel(root, node => {
    path.push(node.val)
    sum += node.val
  }, node => {
    if (!node.left && !node.right && sum === targetSum) {
      result.push(Array.from(path))
    }
    path.pop()
    sum -= node.val
  })

  return result
};
